const storageKey = "portfolio-language";

function getPreferredLanguage() {
    const savedLanguage = window.localStorage.getItem(storageKey);

    if (savedLanguage && translations[savedLanguage]) {
        return savedLanguage;
    }

    const browserLanguage = navigator.language.toLowerCase();

    if (browserLanguage.startsWith("en")) {
        return "en";
    }

    if (browserLanguage.startsWith("es")) {
        return "es";
    }

    return "pt";
}

function applyLanguage(language) {
    const selectedLanguage = translations[language] ? language : "pt";
    const dictionary = translations[selectedLanguage];

    document.documentElement.lang = dictionary.htmlLang;
    document.title = dictionary.documentTitle;

    document.querySelectorAll("[data-i18n]").forEach((element) => {
        const key = element.dataset.i18n;
        element.textContent = dictionary[key];
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
        const key = element.dataset.i18nPlaceholder;
        element.placeholder = dictionary[key];
    });

    document.querySelectorAll("[data-i18n-value]").forEach((element) => {
        const key = element.dataset.i18nValue;
        element.value = dictionary[key];
    });

    document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
        const key = element.dataset.i18nAlt;
        element.alt = dictionary[key];
    });

    document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
        const key = element.dataset.i18nAriaLabel;
        element.setAttribute("aria-label", dictionary[key]);
    });

    const languageSelect = document.querySelector("#language-select");

    if (languageSelect) {
        languageSelect.value = selectedLanguage;
    }

    window.localStorage.setItem(storageKey, selectedLanguage);
}

function closeMenu() {
    const navLinks = document.querySelector("#nav-links");
    const menuButton = document.querySelector("#menu-hamburger");

    if (!navLinks || !menuButton) {
        return;
    }

    navLinks.classList.remove("is-open");
    menuButton.setAttribute("aria-expanded", "false");
}

function toggleMenu() {
    const navLinks = document.querySelector("#nav-links");
    const menuButton = document.querySelector("#menu-hamburger");

    if (!navLinks || !menuButton) {
        return;
    }

    const isOpen = navLinks.classList.toggle("is-open");
    menuButton.setAttribute("aria-expanded", String(isOpen));
}

document.addEventListener("DOMContentLoaded", () => {
    const languageSelect = document.querySelector("#language-select");
    const menuButton = document.querySelector("#menu-hamburger");

    applyLanguage(getPreferredLanguage());

    if (languageSelect) {
        languageSelect.addEventListener("change", (event) => {
            applyLanguage(event.target.value);
        });
    }

    if (menuButton) {
        menuButton.addEventListener("click", toggleMenu);
    }

    document.querySelectorAll("#nav-links a").forEach((link) => {
        link.addEventListener("click", closeMenu);
    });
});
