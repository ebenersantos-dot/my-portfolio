/* ═══════════════════════════════════════════════════════════
   I18N
═══════════════════════════════════════════════════════════ */
const storageKey = "portfolio-language";

function getPreferredLanguage() {
    const saved = window.localStorage.getItem(storageKey);
    if (saved && translations[saved]) return saved;

    const lang = navigator.language.toLowerCase();
    if (lang.startsWith("en")) return "en";
    if (lang.startsWith("es")) return "es";
    return "pt";
}

function applyLanguage(language) {
    const selected = translations[language] ? language : "pt";
    const dict = translations[selected];

    document.documentElement.lang = dict.htmlLang;
    document.title = dict.documentTitle;

    document.querySelectorAll("[data-i18n]").forEach((el) => {
        const key = el.dataset.i18n;
        if (dict[key] !== undefined) el.textContent = dict[key];
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
        const key = el.dataset.i18nPlaceholder;
        if (dict[key] !== undefined) el.placeholder = dict[key];
    });

    document.querySelectorAll("[data-i18n-value]").forEach((el) => {
        const key = el.dataset.i18nValue;
        if (dict[key] !== undefined) el.value = dict[key];
    });

    document.querySelectorAll("[data-i18n-alt]").forEach((el) => {
        const key = el.dataset.i18nAlt;
        if (dict[key] !== undefined) el.alt = dict[key];
    });

    document.querySelectorAll("[data-i18n-aria-label]").forEach((el) => {
        const key = el.dataset.i18nAriaLabel;
        if (dict[key] !== undefined) el.setAttribute("aria-label", dict[key]);
    });

    const languageSelect = document.querySelector("#language-select");
    if (languageSelect) languageSelect.value = selected;

    window.localStorage.setItem(storageKey, selected);
}

/* ═══════════════════════════════════════════════════════════
   MOBILE MENU
═══════════════════════════════════════════════════════════ */
function closeMenu() {
    const navLinks = document.querySelector("#nav-links");
    const menuButton = document.querySelector("#menu-hamburger");
    if (!navLinks || !menuButton) return;

    navLinks.classList.remove("is-open");
    menuButton.setAttribute("aria-expanded", "false");
    document.body.style.overflow = "";
}

function toggleMenu() {
    const navLinks = document.querySelector("#nav-links");
    const menuButton = document.querySelector("#menu-hamburger");
    if (!navLinks || !menuButton) return;

    const isOpen = navLinks.classList.toggle("is-open");
    menuButton.setAttribute("aria-expanded", String(isOpen));
    document.body.style.overflow = isOpen ? "hidden" : "";
}

/* ═══════════════════════════════════════════════════════════
   SCROLL REVEAL
═══════════════════════════════════════════════════════════ */
function initScrollReveal() {
    const elements = document.querySelectorAll(".reveal");

    if (!elements.length) return;

    const observer = new IntersectionObserver(
        (entries) => {
            entries.forEach((entry) => {
                if (entry.isIntersecting) {
                    entry.target.classList.add("is-visible");
                    observer.unobserve(entry.target);
                }
            });
        },
        { threshold: 0.12, rootMargin: "0px 0px -40px 0px" }
    );

    elements.forEach((el) => observer.observe(el));
}

/* ═══════════════════════════════════════════════════════════
   ACTIVE NAV LINKS
═══════════════════════════════════════════════════════════ */
function initActiveNav() {
    const sections = document.querySelectorAll("main section[id], footer[id]");
    const navLinks = document.querySelectorAll("#nav-links a[href^='#']");

    if (!sections.length || !navLinks.length) return;

    const observer = new IntersectionObserver(
        (entries) => {
            entries.forEach((entry) => {
                if (entry.isIntersecting) {
                    const id = entry.target.id;
                    navLinks.forEach((link) => {
                        const href = link.getAttribute("href").replace("#", "");
                        link.classList.toggle("active", href === id);
                    });
                }
            });
        },
        { threshold: 0.4 }
    );

    sections.forEach((section) => observer.observe(section));
}

/* ═══════════════════════════════════════════════════════════
   HEADER SCROLL STATE
═══════════════════════════════════════════════════════════ */
function initHeaderScroll() {
    const header = document.querySelector("#header");
    if (!header) return;

    const update = () => {
        header.classList.toggle("scrolled", window.scrollY > 20);
    };

    window.addEventListener("scroll", update, { passive: true });
    update();
}

